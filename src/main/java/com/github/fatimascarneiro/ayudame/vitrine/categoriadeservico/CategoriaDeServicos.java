package com.github.fatimascarneiro.ayudame.vitrine.categoriadeservico;

public class CategoriaDeServicos {

    private int categoriaDeServicosKey;

    private String nome;

    private String descricao;

    public int getCategoriaDeServicosKey() {
        return categoriaDeServicosKey;
    }

    public void setCategoriaDeServicosKey(int categoriaDeServicosKey) {
        this.categoriaDeServicosKey = categoriaDeServicosKey;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
}
